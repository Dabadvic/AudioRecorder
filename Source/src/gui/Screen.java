    /**
     * Función que se ejecuta al finalizar la aplicación.
     *
     * Se encarga de finalizar correctamente el programa.
     */
    private void finAplicacion() {
        int respuesta;
        respuesta = javax.swing.JOptionPane.showConfirmDialog(rootPane,
                "¿Realmente desea salir?",
                "Salir de la aplicación",
                javax.swing.JOptionPane.OK_CANCEL_OPTION,
                javax.swing.JOptionPane.QUESTION_MESSAGE);
        if (respuesta == javax.swing.JOptionPane.OK_OPTION) {
            this.setVisible(false);
            this.dispose();

            //Se borra el archivo temporal
            File tmp = new File("tmp.wav");
            tmp.delete();
        }
    }

    /**
     * Procedimiento que guarda el archivo.
     * 
     * Para ello, coge el archivo origen temporal que genera el programa y lo
     * copia en la nueva ubicación elegida. Además no es necesario añadir la
     * terminación ".wav" ya que el procedimiento se encarga de añadirla antes
     * de guardar el archivo.
     * 
     * En caso de no elegir ubicación, no se guarda el archivo.
     */
    private void guardar() {
        File tmp = new File("tmp.wav");
        File nuevo;
        int returnVal = chooser.showSaveDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            nuevo = chooser.getSelectedFile();
            //Se añade la terminación .wav al archivo
            File def = new File(nuevo.getPath() + ".wav");
            try {
                Files.copy(tmp.toPath(), def.toPath());
            } catch (IOException ex) {
                Logger.getLogger(Screen.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println("Incorrecto");
                javax.swing.JOptionPane.showMessageDialog(this,
                        "No se ha podido guardar.",
                        "Mensaje",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } else {
            System.out.println("No se ha guardado el archivo");
        }
    }

