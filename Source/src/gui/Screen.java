    /**
     * Función que reproduce un archivo de audio tipo wav.
     *
     * Abre el archivo como entrada de audio y lo reproduce con la interfaz
     * clip, que es la que tiene los métodos necesarios para ello.
     *
     * @param name Ubicación del archivo
     */
    private void playwav(String name) {
        try {
            AudioInputStream audioInputStream = AudioSystem.getAudioInputStream(new File(name).getAbsoluteFile());
            Clip clip = AudioSystem.getClip();
            clip.open(audioInputStream);
            clip.start();
            while (!clip.isRunning()) {
                Thread.sleep(10);
            }
            while (clip.isRunning()) {
                Thread.sleep(10);
            }
            clip.close();
        } catch (Exception ex) {
            System.out.println("Error with playing sound.");
            ex.printStackTrace();
        }
    }

